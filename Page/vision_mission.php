        <b>Visi SMK Negeri 1 Rota Bayat</b> 
        <p>
        Menjadi pusat pengembangan seni budaya dan teknologi berbasis kearifan lokal berdaya saing global
        </p>
        <b>Misi SMK Negeri 1 rota Bayat</b> 
        <p>
        1. Menanamkan nilai-nilai religi dan karakteristik bangsa Indonesia.</br>
        2. Menyelenggarakan pendidikan kejuruan bermutu sesuai kebutuhan dunia usaha / dunia industri bidang kerajinan, teknologi informasi komunikasi dan otomotif.</br>
        3. Memberikan bekal kemandirian berwawasan lingkungan.</br>
        4. Melestarikan dan mengembangkan budaya Indonesia.</br>
        </p>
        <b>Tujuan SMK Negeri 1 Rota Bayat</b> 
        <p>
        1. Terciptanya lingkungan wiyata mandala yang aman, nyaman, dan sehat.</br>
        2. Peserta didik berakhlaq, berbudi pekerti luhur, cerdas, terampil, mandiri, kreatif, dan inovatif.</br>
        3. Maju bersama masyarakat sekitar.</br>
        4. Lestarinya nilai-nilai luhur budaya bangsa Indonesia.</br>
        </p>
    